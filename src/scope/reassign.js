//Usando var:
var firstName; //declaración
firstName = 'Oscar'; //asignación
console.log(firstName);

var lastName = 'David'; //declaración y asignación
lastName = 'Ana'; //reasignación
console.log(lastName);

var secondName = 'David';
var secondName = 'Ana'; //redeclaración
console.log(secondName);

//Usando let
let fruit = 'Apple'; //delaración y asignación
fruit = 'Kiwi'; //reasignación
console.log(fruit);

let fruit = 'Banana'; //redeclaración
console.log(fruit);

//Usando const
const animal = 'dog'; //delaración y asignación
animal = 'cat'; //reasignación
console.log(animal);

const animal = 'snake'; //redeclaración

